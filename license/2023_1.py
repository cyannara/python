a = {'한국', '중국', '일본'}                # 초기 집합 생성
a.add('베트남')                            # '베트남' 추가
a.add('중국')                              # '중국'은 이미 있으므로 무시됨
a.remove('일본')                          # '일본' 제거
a.update({'홍콩', '한국', '태국'})         # 여러 요소 한 번에 추가 (중복은 무시)

print(a)                                  # 최종 집합 출력